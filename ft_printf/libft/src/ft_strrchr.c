#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	size_t	i;

	i = ft_strlen(str);
	while (*str != '\0')
		str++;
	if (c == 0)
		return ((char *)str);
	while (*str != c && i >= 0)
	{
		if (i == 0)
			return (0);
		str--;
		i--;
	}
	return ((char *)str);
}
