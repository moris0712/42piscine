#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned int	len;
	unsigned int	i;
	char			*str;

	len = ft_strlen(s1);
	str = (char *)malloc((len + 1) * sizeof(char));
	if (str == NULL)
		return (0);
	i = 0;
	while (i < len)
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
